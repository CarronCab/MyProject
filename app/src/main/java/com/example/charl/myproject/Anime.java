package com.example.charl.myproject;

public class Anime {


    private String name;
    private String url;


    public Anime(String name) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {return url;}

    public void setUrl(String url) { this.url = url; }
}
